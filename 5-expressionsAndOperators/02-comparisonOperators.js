// Equal 
'3' == 3;
console.log('3' == 3);

// JavaScript is helpful and does something known as coercion when comparing values
// coercion is the process of convering a value form one datatype to another

// strict equal - this overrides javascipt coercion
3 === 3;
console.log('3' === 3);

// Not Equal
console.log('3' != 3);

// strict not equal
'3' !== 4;

// greater
3 > 2;

// less than
2 < 3;

// Greater than or equal to
3 >= 2; 
// not to be confused with =>(fat arrow functions)

// LESS THAN OR EQUAL TO
2 <= 3;
​
// AND
2 && 3
​
